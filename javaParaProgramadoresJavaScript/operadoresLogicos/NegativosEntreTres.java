public class NegativosEntreTres{
    public static void main(String[] args){
        int a = 40;
        int b = -25;
        int c = 1;

        if( a < 0 || b < 0 || c < 0){
            System.out.println("Existe um numero negativo!");
        }else{
            System.out.println("Nenhum numero negativo");
        }
    }
}