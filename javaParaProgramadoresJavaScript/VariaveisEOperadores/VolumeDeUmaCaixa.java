public class VolumeDeUmaCaixa{
    public static void main(String[] args){
        float base = 6.55f;
        float altura = 9.44f;
        float profundidade = 22.6f;
        float resposta;

        resposta = base * altura * profundidade;
        System.out.println("Resultado do volume de uma caixa:");
        System.out.println(resposta);
        System.out.println("***************************");
    }
}